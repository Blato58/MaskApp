#!/usr/bin/env python3
"""Generate exact MAUI built-in preview assets from the decompiled Android catalog."""

from __future__ import annotations

import argparse
import hashlib
import json
import shutil
import sys
import tempfile
import xml.etree.ElementTree as ET
from dataclasses import dataclass
from pathlib import Path

from PIL import Image


PREVIEW_WIDTH = 52
PREVIEW_HEIGHT = 64
ANIMATION_FRAME_DURATION_MS = 100
ANIMATION_IDS = (0, 1, 2, 3, *range(5, 46))
PROVENANCE = "Original Android catalog preview"


@dataclass(frozen=True)
class PreviewDefinition:
    asset_type: str
    command_id: int
    resource_name: str
    frame_count: int
    frame_duration_ms: int

    @property
    def file_name(self) -> str:
        prefix = "face" if self.asset_type == "StaticImage" else "anim"
        extension = "png" if self.asset_type == "StaticImage" else "gif"
        return f"builtin_{prefix}_{self.command_id:02d}.{extension}"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--check",
        action="store_true",
        help="Regenerate into a temporary directory and fail if tracked outputs differ.",
    )
    return parser.parse_args()


def repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def resource_arrays(path: Path) -> dict[str, list[str]]:
    root = ET.parse(path).getroot()
    arrays: dict[str, list[str]] = {}
    for node in root.findall("array"):
        name = node.attrib.get("name")
        if not name:
            continue
        values = []
        for item in node.findall("item"):
            value = (item.text or "").strip()
            if not value.startswith("@mipmap/"):
                raise ValueError(f"Unexpected resource value in {name}: {value}")
            values.append(value.removeprefix("@mipmap/"))
        arrays[name] = values
    return arrays


def resource_files(directory: Path) -> dict[str, Path]:
    files: dict[str, Path] = {}
    for path in directory.iterdir():
        if not path.is_file():
            continue
        if path.stem in files:
            raise ValueError(f"Duplicate resource stem: {path.stem}")
        files[path.stem] = path
    return files


def load_frame(path: Path) -> Image.Image:
    with Image.open(path) as image:
        frame = image.convert("RGBA")
    if frame.size != (PREVIEW_WIDTH, PREVIEW_HEIGHT):
        raise ValueError(f"Unexpected dimensions for {path}: {frame.size}")
    return frame


def build_definitions(arrays: dict[str, list[str]]) -> tuple[list[PreviewDefinition], dict[tuple[str, int], list[str]]]:
    face_resources = arrays.get("image_default_new")
    if face_resources is None or len(face_resources) != 70:
        raise ValueError("image_default_new must contain exactly 70 resources")

    definitions: list[PreviewDefinition] = []
    resources: dict[tuple[str, int], list[str]] = {}
    for command_id, resource in enumerate(face_resources):
        definitions.append(PreviewDefinition("StaticImage", command_id, resource, 1, 0))
        resources[("StaticImage", command_id)] = [resource]

    for command_id in ANIMATION_IDS:
        name = f"anim{command_id}"
        frames = arrays.get(name)
        if not frames:
            raise ValueError(f"Missing or empty animation array: {name}")
        definitions.append(
            PreviewDefinition(
                "Animation",
                command_id,
                frames[0],
                len(frames),
                ANIMATION_FRAME_DURATION_MS,
            )
        )
        resources[("Animation", command_id)] = frames

    if len(definitions) != 115:
        raise ValueError(f"Expected 115 preview definitions, found {len(definitions)}")
    return definitions, resources


def generate_assets(
    output_directory: Path,
    definitions: list[PreviewDefinition],
    resources: dict[tuple[str, int], list[str]],
    files: dict[str, Path],
) -> None:
    output_directory.mkdir(parents=True, exist_ok=True)
    for definition in definitions:
        frame_names = resources[(definition.asset_type, definition.command_id)]
        frames = [load_frame(files[name]) for name in frame_names]
        destination = output_directory / definition.file_name
        if definition.asset_type == "StaticImage":
            frames[0].save(destination, format="PNG", optimize=False)
            continue

        frames[0].save(
            destination,
            format="GIF",
            save_all=True,
            append_images=frames[1:],
            duration=ANIMATION_FRAME_DURATION_MS,
            loop=0,
            disposal=2,
            optimize=False,
        )


def generate_catalog(path: Path, definitions: list[PreviewDefinition]) -> None:
    lines = [
        "// <auto-generated />",
        "namespace MaskApp.Core.Features.BuiltIns;",
        "",
        "internal static class BuiltInPreviewCatalogGenerated",
        "{",
        "    public static IReadOnlyDictionary<(BuiltInAssetType Type, int Id), BuiltInAssetPreview> All { get; } =",
        "        new Dictionary<(BuiltInAssetType Type, int Id), BuiltInAssetPreview>",
        "        {",
    ]
    for definition in definitions:
        animated = "true" if definition.asset_type == "Animation" else "false"
        lines.append(
            "            "
            f"[(BuiltInAssetType.{definition.asset_type}, {definition.command_id})] = "
            f'new BuiltInAssetPreview("{definition.file_name}", {PREVIEW_WIDTH}, {PREVIEW_HEIGHT}, '
            f"{animated}, {definition.frame_count}, {definition.frame_duration_ms}, \"{PROVENANCE}\"),"
        )
    lines.extend(["        };", "}", ""])
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines), encoding="utf-8", newline="\n")


def sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as stream:
        for chunk in iter(lambda: stream.read(128 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def generate_manifest(path: Path, asset_directory: Path, definitions: list[PreviewDefinition]) -> None:
    payload = {
        "source": "decompiled-app/app/src/main/res/values/arrays.xml",
        "dimensions": {"width": PREVIEW_WIDTH, "height": PREVIEW_HEIGHT},
        "animationFrameDurationMs": ANIMATION_FRAME_DURATION_MS,
        "items": [
            {
                "type": definition.asset_type,
                "id": definition.command_id,
                "file": definition.file_name,
                "frameCount": definition.frame_count,
                "sha256": sha256(asset_directory / definition.file_name),
            }
            for definition in definitions
        ],
    }
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8", newline="\n")


def generate(root: Path, output_root: Path) -> list[Path]:
    arrays_path = root / "decompiled-app/app/src/main/res/values/arrays.xml"
    resource_directory = root / "decompiled-app/app/src/main/base.apk/res/mipmap-xxhdpi-v4"
    if not arrays_path.is_file() or not resource_directory.is_dir():
        raise FileNotFoundError("The decompiled Android arrays and mipmap resources are required to regenerate previews")

    arrays = resource_arrays(arrays_path)
    files = resource_files(resource_directory)
    definitions, resources = build_definitions(arrays)
    missing = sorted({name for names in resources.values() for name in names if name not in files})
    if missing:
        raise FileNotFoundError(f"Missing referenced mipmap resources: {', '.join(missing)}")

    asset_directory = output_root / "src/MaskApp.App/Resources/Images/builtins"
    catalog_path = output_root / "src/MaskApp.Core/Features/BuiltIns/BuiltInPreviewCatalog.Generated.cs"
    manifest_path = output_root / "build/builtin-preview-manifest.json"
    generate_assets(asset_directory, definitions, resources, files)
    generate_catalog(catalog_path, definitions)
    generate_manifest(manifest_path, asset_directory, definitions)
    return [asset_directory, catalog_path, manifest_path]


def compare_directory(expected: Path, actual: Path) -> list[str]:
    problems: list[str] = []
    expected_files = {path.relative_to(expected) for path in expected.rglob("*") if path.is_file()}
    actual_files = {path.relative_to(actual) for path in actual.rglob("*") if path.is_file()}
    for missing in sorted(expected_files - actual_files):
        problems.append(f"missing: {actual / missing}")
    for extra in sorted(actual_files - expected_files):
        problems.append(f"extra: {actual / extra}")
    for relative in sorted(expected_files & actual_files):
        if (expected / relative).read_bytes() != (actual / relative).read_bytes():
            problems.append(f"stale: {actual / relative}")
    return problems


def check(root: Path) -> int:
    with tempfile.TemporaryDirectory(prefix="maskapp-builtins-") as temp:
        generated_root = Path(temp)
        generated = generate(root, generated_root)
        problems: list[str] = []
        for expected in generated:
            relative = expected.relative_to(generated_root)
            actual = root / relative
            if expected.is_dir():
                if not actual.is_dir():
                    problems.append(f"missing directory: {actual}")
                else:
                    problems.extend(compare_directory(expected, actual))
            elif not actual.is_file():
                problems.append(f"missing: {actual}")
            elif expected.read_bytes() != actual.read_bytes():
                problems.append(f"stale: {actual}")

        if problems:
            print("Built-in preview outputs are not current:", file=sys.stderr)
            for problem in problems:
                print(f"- {problem}", file=sys.stderr)
            return 1
    print("Built-in preview outputs are current (70 faces, 45 animations).")
    return 0


def main() -> int:
    args = parse_args()
    root = repo_root()
    if args.check:
        return check(root)

    asset_directory = root / "src/MaskApp.App/Resources/Images/builtins"
    if asset_directory.exists():
        shutil.rmtree(asset_directory)
    generate(root, root)
    print("Generated 70 face previews and 45 animation previews.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
